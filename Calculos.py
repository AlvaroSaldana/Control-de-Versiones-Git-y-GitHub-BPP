def suma(a,b):
    """
    Esta función hace una suma
    Suma los valores asignados de a y de b"""

    return a + b 
    """
    Devuelve resultado la suma de los valores de a más b"""


def resta (a,b):
    """
    Esta función hace una resta
    resta los valores asignados de a y b"""

    return a-b 
    """
    Devuelve resultado la resta de los valores de a-b"""

def multiplicacion (a,b):
    """
    Esta función hace una multiplicación
    multiplica los valores asignados de a y b"""

    return a*b
    """
     Devuelve resultado de la multiplicación de los valores asignados de a por b"""

def division (a,b):
    """
    Esta función hace una división
    divide los valores asignados de a y b"""
    
    return a/b
